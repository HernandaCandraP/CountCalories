    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        manfaat_makanan kat = new manfaat_makanan();
        kat.setJenismakanan(txtjenis.getText());
        kat.setManfaat(txtmanfaat.getText());
        kat.setIdmanfaat(Integer.parseInt(txtIdmanfaat.getText()));
        kat.save();
        txtIdmanfaat.setText(Integer.toString(kat.getIdmanfaat()));
        tampilkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

